  // 把任意语言代码归一化到受支持的界面语言;无匹配则英文。
  function resolve(code) {
    if (!code) return 'en';
    if (S[code]) return code;
    const low = String(code).toLowerCase();
    if (low.startsWith('zh')) return (low.includes('tw') || low.includes('hk') || low.includes('hant')) ? 'zh-TW' : 'zh-CN';
    const base = low.split('-')[0];
    return S[base] ? base : 'en';
  }

  // 首次打开:跟随浏览器界面语言,无匹配则英文。
  function detect() {
    try {
      const nav = (typeof chrome !== 'undefined' && chrome.i18n && chrome.i18n.getUILanguage)
        ? chrome.i18n.getUILanguage() : (navigator.language || 'en');
      return resolve(nav);
    } catch (e) { return 'en'; }
  }

  function t(ui, key, ...args) {
    const table = S[ui] || S.en;
    const v = (table[key] !== undefined) ? table[key] : S.en[key];
    return typeof v === 'function' ? v(...args) : (v !== undefined ? v : key);
  }

  window.ROSETTA_I18N = { t, resolve, detect, STRINGS: S, UI_LANGS };
